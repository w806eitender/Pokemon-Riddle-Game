<p>
&nbsp;</p>
</div>
<div>
　　田尻智看中了 GAME BOY 獨特的「交換」功能，並以他鍾愛的《<a class="acglink" href="http://acg.gamer.com.tw/search.php?encode=utf8&amp;kw=%E8%B6%85%E4%BA%BA%E5%8A%9B%E9%9C%B8%E7%8E%8B" target="_blank">超人力霸王</a>》劇中「膠囊怪獸（カプセル怪獣）」為靈感，創作出了一份命名為「轉蛋怪獸（カプセルモンスター）」的 RPG 企劃書。據說一開始田尻智是將「轉蛋怪獸」拿到 NAMCO 毛遂自薦，不過遭到閉門羹才轉至任天堂提案並在 1990 年獲得採用，得到了一筆開發的資金。而之後「轉蛋怪獸（カプセルモンスター）」因為商標權的問題和簡稱「轉獸（カプモン）」的發音不是挺順口，所以乾脆就將企劃變更為「口袋怪獸（ポケットモンスター）」（編按：台譯為《神奇寶貝》）。</div>
<div>
&nbsp;</div>
<div>
　　雖然《神奇寶貝》是由 GAME FREAK 發想並開發，不過真的能夠問世及爆紅，任天堂對於當年沒沒無聞的 GAME FREAK 長達六年的支持和等待實在功不可沒。當年《<a class="acglink" href="http://acg.gamer.com.tw/search.php?encode=utf8&amp;kw=%E7%A5%9E%E5%A5%87%E5%AF%B6%E8%B2%9D" target="_blank">神奇寶貝</a>》其實預定在 1991 年就要交貨上市，不過缺乏 RPG 開發經驗的 GAME FREAK 並沒辦法如期完成。在其中 GAME FREAK 為了補足資金缺口，也一陣一陣的接受其他遊戲的程式外包，《<a class="acglink" href="http://acg.gamer.com.tw/search.php?encode=utf8&amp;kw=%E7%A5%9E%E5%A5%87%E5%AF%B6%E8%B2%9D" target="_blank">神奇寶貝</a>》的開發就陷入一個半調子的瓶頸...</div>
