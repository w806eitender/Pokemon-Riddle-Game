<h1>
《神奇寶貝》遊戲的誕生</h1>
<div>
&nbsp;</div>
<div>
　　提起當年開發《神奇寶貝》遊戲的契機，是由遊戲開發商「GAME FREAK」的製作人「田尻智」看中了攜帶型遊戲主機「GAME BOY」平台上，由 SQUARE 所推出的 RPG 遊戲《<a class="acglink" href="http://acg.gamer.com.tw/search.php?encode=utf8&amp;kw=%E9%AD%94%E7%95%8C%E5%A1%94%E5%A3%ABSa%E3%83%BBGa" target="_blank">魔界塔士Sa・Ga</a>》獲得了相當不錯的成績，所以發現了其實掌上型主機上玩家不只追求簡單的益智或是動作遊戲，玩家同時也追求可以帶在身上遊玩的 RPG 遊戲，所以興起了在 GB 推出 RPG 遊戲的念頭。</div>
<div>
<ul class="GN-thumbnails">
<li class="line">
<div class="GN-thumbnail">
<img  name="gnnPIC" class="lazyload" data-sizes="auto" data-src="https://p2.bahamut.com.tw/B/2KU/68/0000889568.JPG" data-srcset="https://p2.bahamut.com.tw/B/2KU/68/0000889568.JPG?w=1000 1x,https://p2.bahamut.com.tw/B/2KU/68/0000889568.JPG 2x" title="" /></div>
</li>
